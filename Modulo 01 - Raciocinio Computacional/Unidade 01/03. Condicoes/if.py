#if
num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

if num1 > num2:
    print(f"{num1} é maior que {num2}.")
    print(f"A diferença entre {num1} e {num2} é {num1 - num2}.")
    print(f"A soma de {num1} e {num2} é {num1 + num2}.")
print("Obrigada.")
