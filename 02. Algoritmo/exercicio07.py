'''Crie um algoritmo que solicita ao usuário que insira dois números e exibe na tela o resultado da soma, subtração, multiplicação e divisão desses números.'''

num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))

soma = num1 + num2
sub = num1 - num2
multi = num1 * num2
div = num1 / num2

print("A soma é:", soma)
print("A subtração é:", sub)
print("A multiplicação é:", multi)
print("A divisão é:", div)